    let currentLang = 'es';

    function setLanguage(lang) {
        if (translations[lang]) {
            currentLang = lang;
            localStorage.setItem('fleetadmin_lang', lang);
            document.documentElement.lang = lang;
        }
    }

    function getLanguage() {
        return currentLang;
    }

    function t(key, params = {}) {
        let text = translations[currentLang]?.[key] || translations['es']?.[key] || key;
        // Reemplazar parámetros {param}
        Object.entries(params).forEach(([k, v]) => {
            text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
        });
        return text;
    }

    function init() {
        const saved = localStorage.getItem('fleetadmin_lang');
        if (saved && translations[saved]) {
            currentLang = saved;
        }
        document.documentElement.lang = currentLang;
    }

    function getAvailableLanguages() {
        return [
            { code: 'es', name: 'Español', flag: '🇪🇸' },
            { code: 'en', name: 'English', flag: '🇺🇸' },
            { code: 'pt', name: 'Português', flag: '🇧🇷' }
        ];
    }

    return { setLanguage, getLanguage, t, init, getAvailableLanguages };
